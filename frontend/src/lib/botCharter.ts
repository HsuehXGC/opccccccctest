/** bot 的有效 charter：显式配置优先，否则用角色默认（并把 bot.skills 合入核心技能） */
export function charterOf(bot: Bot): BotCharter {
  if (bot.charter) return bot.charter
  const base = DEFAULT_CHARTERS[bot.role]
  const coreSkills = Array.from(new Set([...(bot.skills ?? []), ...base.coreSkills]))
  return { ...base, coreSkills }
}

/** 把 charter 组装成执行时 prepend 到任务 brief 前的 system prompt */
export function assembleSystemPrompt(bot: Bot, charter: BotCharter = charterOf(bot)): string {
  const list = (arr: string[]) => (arr.length ? arr.map((x) => `- ${x}`).join('\n') : '- （待补充）')
  return [
    `你是虚拟员工「${bot.name}」，担任${bot.role}。`,
    ``,
    `## 定位`,
    charter.mission || '（待补充）',
    ``,
    `## 职责范围（你应当做）`,
    list(charter.canDo),
    ``,
    `## 边界（你不应做）`,
    list(charter.cannotDo),
    ``,
    `## 核心技能`,
    charter.coreSkills.join(' · ') || '（待补充）',
    ``,
    `执行任务时，严格遵守上述职责与边界，发挥核心技能，产出符合验收标准的成果；遇到超出边界的要求应拒绝并说明。`,
  ].join('\n')
}
